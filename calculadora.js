function sumar(a,b){
    return a + b;
}

function restar(a,b){
    return a - b;
}

function multiplicar(a, b){
    return a * b;
}

function dividir(a, b){
    return a / b;
}

function potenciacion(a, b){
    return a ** b;
}

function raizCuadrada(a){
    return Math.sqrt(a);
}

console.log(sumar(5, 10));
console.log(restar(10, 5));
console.log(multiplicar(10, 10));
console.log(dividir(20, 5));
console.log(potenciacion(3, 3));
console.log(raizCuadrada(9));